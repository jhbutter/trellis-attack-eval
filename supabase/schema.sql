-- Create this table in Supabase SQL Editor if you want centralized multi-user result collection.
-- For a controlled research study, restrict RLS policies more tightly before public deployment.

create table if not exists public.ratings_submissions (
  id bigint generated by default as identity primary key,
  submission_id text not null unique,
  session_id text not null,
  user_id text,
  batch_id text not null,
  completed_count integer not null,
  total_count integer not null,
  started_at timestamptz not null,
  submitted_at timestamptz not null,
  payload jsonb not null,
  created_at timestamptz not null default now()
);

alter table public.ratings_submissions enable row level security;

-- Demo policy: anonymous users can insert submissions.
-- Production recommendation: require login or issue evaluator tokens.
create policy "allow anonymous insert submissions"
  on public.ratings_submissions
  for insert
  to anon
  with check (true);

